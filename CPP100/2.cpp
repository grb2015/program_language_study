/* 题目：企业发放的奖金根据利润提成。
利润(I)低于或等于10万元时，奖金可提10%；
利润高于10万元，低于20万元时，低于10万元的部分按10%提成，高于10万元的部分，可提成7.5%；
20万到40万之间时，高于20万元的部分，可提成5%；
40万到60万之间时高于40万元的部分，可提成3%；
60万到100万之间时，高于60万元的部分，可提成1.5%；
高于100万元时，超过100万元的部分按1%提成。
从键盘输入当月利润I，求应发放奖金总数？
*/
#include<iostream>
using namespace std;
int main(){
    float p=12; // 利润(万元)
    float b;  // 奖金
    if(p<=10){
        b = p*0.1;
    }else if(p>10 && p<=20){
        b = 10*0.1 + (p-10)*0.075;
    }else if(p>20 && p<=40){
        b = 10*0.1 +(20-10)*0.075+(p-20)*0.05;
    }else if(p>40 && p<=60){
        b = 10*0.1+(20-10)*0.075+(40-20)*0.05+(p-40)*0.03;
    }else if(p>60 && p<=100){
        b = 10*0.1+(20-10)*0.075+(40-20)*0.05+(60-40)*0.03+(p-60)*0.015;
    }else if(p>100){
        b = 10*0.1+(20-10)*0.075+(40-20)*0.05+(60-40)*0.03+(100-60)*0.015+(p-100)*0.01;
    }
    printf("%f",b);
}
